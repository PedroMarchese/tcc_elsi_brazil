def rodar_todas_regressoes(df):
    ols_results_raw = []
    logistic_results_raw = []

    predictors = ['SSWS', 'LRI']

    # OLS Dependent Variables (continuous)
    ols_desfechos = ['n_morbidades'] + [
        'soma_grupo_0_neuro_oftalmo_musculoesqueleticas',
        'soma_grupo_1_cardiorrespiratorias',
        'soma_grupo_2_outras'
    ]

    # Logistic Dependent Variables (binary)
    logistic_desfechos = [
        'grupo_0_neuro_oftalmo_musculoesqueleticas',
        'grupo_1_cardiorrespiratorias',
        'grupo_2_outras'
    ] + [
        'morbidades_menor_que_2', 'morbidades_igual_a_2',
        'morbidades_igual_a_3', 'morbidades_igual_a_4',
        'morbidades_igual_a_5', 'morbidades_igual_a_6',
        'morbidades_maior_ou_igual_a_7'
    ]

    # Run OLS Regressions
    for desfecho in ols_desfechos:
        for preditor in predictors:
            # Non-adjusted
            ols_results_raw.append(rodar_ols(df, desfecho, preditor, ajustar=False))
            # Adjusted
            ols_results_raw.append(rodar_ols(df, desfecho, preditor, ajustar=True))

    # Run Logistic Regressions
    for desfecho in logistic_desfechos:
        for preditor in predictors:
            # Non-adjusted
            logistic_results_raw.append(rodar_logistica(df, desfecho, preditor, ajustar=False))
            # Adjusted
            logistic_results_raw.append(rodar_logistica(df, desfecho, preditor, ajustar=True))

    # Extract metrics and create DataFrames
    resultados_ols = pd.DataFrame([extrair_metricas_modelo(r) for r in ols_results_raw])
    resultados_logistica = pd.DataFrame([extrair_metricas_modelo(r) for r in logistic_results_raw])

    # Define column orders for OLS and Logistic results
    ols_ordem_colunas = [
        'tipo_modelo', 'desfecho', 'preditor', 'ajustado', 'formula', 'n',
        'coef', 'std_err', 'stat', 'p_value',
        'ic95_inf', 'ic95_sup',
        'r_squared', 'r_squared_ajustado',
        'aic', 'bic'
    ]
    logistic_ordem_colunas = [
        'tipo_modelo', 'desfecho', 'preditor', 'ajustado', 'formula', 'n',
        'coef', 'std_err', 'stat', 'p_value',
        'ic95_inf', 'ic95_sup',
        'pseudo_r_squared',
        'aic', 'bic',
        'odds_ratio', 'or_ic95_inf', 'or_ic95_sup'
    ]

    resultados_ols = resultados_ols[ols_ordem_colunas].copy()
    resultados_logistica = resultados_logistica[logistic_ordem_colunas].copy()

    # Round numerical columns for clean display
    colunas_numericas_para_arredondar = [
        'coef', 'std_err', 'stat', 'p_value',
        'ic95_inf', 'ic95_sup',
        'r_squared', 'r_squared_ajustado', 'pseudo_r_squared',
        'aic', 'bic',
        'odds_ratio', 'or_ic95_inf', 'or_ic95_sup'
    ]

    for col in colunas_numericas_para_arredondar:
        if col in resultados_ols.columns:
            resultados_ols[col] = resultados_ols[col].round(4)
        if col in resultados_logistica.columns:
            resultados_logistica[col] = resultados_logistica[col].round(4)

    return resultados_ols, resultados_logistica

