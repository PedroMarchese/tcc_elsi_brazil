def interpretar_efeito(linha):
    preditor = linha['preditor']
    desfecho = linha['desfecho']
    tipo_modelo = linha['tipo_modelo']
    coef = linha['coef']
    p_value = linha['p_value']

    if pd.isna(coef) or pd.isna(p_value):
        return 'interpretação indisponível'

    if p_value >= 0.05:
        return f'sem associação estatisticamente significativa entre {preditor} e {desfecho}'

    if tipo_modelo == 'OLS':
        if coef > 0:
            return f'maior {preditor} associado a maior {desfecho}'
        elif coef < 0:
            return f'maior {preditor} associado a menor {desfecho}'
        else:
            return f'ausência de efeito aparente de {preditor} sobre {desfecho}'

    if tipo_modelo == 'Logística':
        if coef > 0:
            return f'maior {preditor} associado a maior chance de {desfecho}'
        elif coef < 0:
            return f'maior {preditor} associado a menor chance de {desfecho}'
        else:
            return f'ausência de efeito aparente de {preditor} sobre {desfecho}'

    return 'interpretação não classificada'

def classificar_direcao(linha):
    coef = linha['coef']
    p_value = linha['p_value']

    if pd.isna(coef) or pd.isna(p_value):
        return 'indeterminado'
    if p_value >= 0.05:
        return 'não significativo'
    if coef > 0:
        return 'positivo'
    if coef < 0:
        return 'negativo'
    return 'nulo'

def classificar_significancia(p):
    if pd.isna(p):
        return 'indisponível'
    elif p < 0.001:
        return 'p<0.001'
    elif p < 0.01:
        return 'p<0.01'
    elif p < 0.05:
        return 'p<0.05'
    else:
        return 'ns'

