def montar_formula(desfecho, preditor, ajustar=False):
    formula = f"{desfecho} ~ {preditor}"
    if ajustar:
        formula += " + idade + sexo"
    return formula

# =========================================================
# 4. FUNÇÕES DE AJUSTE DOS MODELOS
# =========================================================

def rodar_ols(df, desfecho, preditor, ajustar=False):
    formula = montar_formula(desfecho, preditor, ajustar=ajustar)

    colunas = [desfecho, preditor]
    if ajustar:
        colunas += ['idade', 'sexo']

    df_modelo = df[colunas].dropna().copy()
    modelo = smf.ols(formula=formula, data=df_modelo).fit()

    return {
        'tipo_modelo': 'OLS',
        'desfecho': desfecho,
        'preditor': preditor,
        'ajustado': ajustar,
        'formula': formula,
        'n': len(df_modelo),
        'modelo': modelo
    }

def rodar_logistica(df, desfecho, preditor, ajustar=False):
    formula = montar_formula(desfecho, preditor, ajustar=ajustar)

    colunas = [desfecho, preditor]
    if ajustar:
        colunas += ['idade', 'sexo']

    df_modelo = df[colunas].dropna().copy()
    modelo = smf.logit(formula=formula, data=df_modelo).fit(disp=False)

    return {
        'tipo_modelo': 'Logística',
        'desfecho': desfecho,
        'preditor': preditor,
        'ajustado': ajustar,
        'formula': formula,
        'n': len(df_modelo),
        'modelo': modelo
    }

